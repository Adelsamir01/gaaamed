const TRIVIA_HISTORY_KEY = 'dedos-trivia-recent-v1'
export const TRIVIA_RECENT_LIMIT = 50

function shuffled<T>(values: T[], random: () => number): T[] {
  const result = [...values]
  for (let index = result.length - 1; index > 0; index -= 1) {
    const swapWith = Math.floor(random() * (index + 1))
    ;[result[index], result[swapWith]] = [result[swapWith]!, result[index]!]
  }
  return result
}

/** Selects a round while keeping recently seen questions out of rotation. */
export function selectTriviaQuestionIds(
  questionCount: number,
  roundSize: number,
  recentQuestionIds: number[] = [],
  random: () => number = Math.random,
): { selectedIds: number[]; nextRecentIds: number[] } {
  const wanted = Math.max(0, Math.min(Math.floor(roundSize), questionCount))
  const recent = [...new Set(recentQuestionIds)]
    .filter((id) => Number.isInteger(id) && id >= 0 && id < questionCount)
    .slice(0, TRIVIA_RECENT_LIMIT)

  // Relax the oldest exclusions only when the bank is too small to fill a round.
  const excluded = new Set(recent)
  while (questionCount - excluded.size < wanted) {
    const oldest = recent.pop()
    if (oldest === undefined) break
    excluded.delete(oldest)
  }

  const candidates = Array.from({ length: questionCount }, (_, id) => id).filter((id) => !excluded.has(id))
  const selectedIds = shuffled(candidates, random).slice(0, wanted)
  const nextRecentIds = [...new Set([...selectedIds, ...recent])].slice(0, TRIVIA_RECENT_LIMIT)
  return { selectedIds, nextRecentIds }
}

function readRecentQuestionIds(): number[] {
  try {
    const parsed = JSON.parse(localStorage.getItem(TRIVIA_HISTORY_KEY) ?? '[]')
    return Array.isArray(parsed) ? parsed.filter((id): id is number => typeof id === 'number') : []
  } catch {
    return []
  }
}

function writeRecentQuestionIds(ids: number[]) {
  try {
    localStorage.setItem(TRIVIA_HISTORY_KEY, JSON.stringify(ids))
  } catch {
    // A restricted WebView can still play; it only loses cross-session history.
  }
}

export function selectTriviaQuestions(roundSize: number, random: () => number = Math.random): TriviaQuestion[] {
  const { selectedIds, nextRecentIds } = selectTriviaQuestionIds(
    TRIVIA_QUESTIONS.length,
    roundSize,
    readRecentQuestionIds(),
    random,
  )
  writeRecentQuestionIds(nextRecentIds)
  return selectedIds.map((id) => TRIVIA_QUESTIONS[id]!)
}
